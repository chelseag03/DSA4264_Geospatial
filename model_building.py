axes[1].barh(r_r2['Model'], r_r2['R²'], color=colors2)
axes[1].set_xlabel('R² — higher is better')
axes[1].set_title('R-Squared')
for i, v in enumerate(r_r2['R²']):
    axes[1].text(v * 0.01, i, f"{v:.4f}", va='center', fontsize=8)

